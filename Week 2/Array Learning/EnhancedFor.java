public class EnhancedFor {
    public static void main(String[] args)
    {
        int nums[]=new int[4];
        nums[0]=4;
        nums[1]=5;
        nums[2]=6;
        nums[3]=7;

        for(int n:nums)
        {
            System.out.println(n);
        }
        //also refer Demo2.java
    }
}
